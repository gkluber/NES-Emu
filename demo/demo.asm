  ;STX $4010    ; disable DMC IRQs

clrmem:
  LDA #$00
  STA $0000, x
  STA $0100, x
  STA $0200, x
  STA $0400, x
  STA $0500, x
  STA $0600, x
  STA $0700, x
  LDA #$FE
  STA $0300, x
  INX
  BNE clrmem

setmem:
  LDX #$00
loop:
  TXA
  STA $0000, x
  INX
  BNE loop

NMI:
  RTI
 
